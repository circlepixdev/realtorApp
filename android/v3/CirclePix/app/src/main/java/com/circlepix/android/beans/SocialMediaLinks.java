public class SocialMediaLinks {
    private String facebook;
    private String twitter;
    private String youtube;
    private String instagram;
    private String pinterest;
    private String blogger;

    public String getFacebookURL() {
        return facebook;
    }
    public void setFacebookURL(String facebook) {
        this.facebook = facebook;
    }

    public String getTwitterURL() {
        return twitter;
    }
    public void setTwitterURL(String twitter) {
        this.twitter = twitter;
    }

    public String getYoutubeURL() {
        return youtube;
    }
    public void setYoutubeURL(String youtube) {
        this.youtube = youtube;
    }

    public String getInstagramURL() {
        return instagram;
    }
    public void setInstagramURL(String instagram) {
        this.instagram = instagram;
    }
    public String getPinterestURL() {
        return pinterest;
    }
    public void setPinterestURL(String pinterest) {
        this.pinterest = pinterest;
    }

    public String getBloggerURL() {
        return blogger;
    }
    public void setBloggerURL(String blogger) {
        this.blogger = blogger;
    }

}
